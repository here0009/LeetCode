# Definition for a binary tree node.
class TreeNode:
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None

class Solution:
    def lowestCommonAncestor(self, root: 'TreeNode', p: 'TreeNode', q: 'TreeNode') -> 'TreeNode':
        def dfs(node):
            if not node:
                return 0
            res = 0
            if node is p or node is q:
                res = 1
            left = dfs(node.left)
            right = dfs(node.right)
            if (left == 1 and right == 1) or ((res == 1) and (left == 1 or right == 1)):
                self.res = node
            return res + left + right

        self.res = None
        dfs(root)
        return self.res



